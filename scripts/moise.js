const isLowerCase = str => str == str.toLowerCase() && str != str.toUpperCase();
function to(string) {
    let res = '';
    for(let i = 0; i < string.length; ++i) {
        if(table[0][string[i].toLowerCase()]) {
            res += isLowerCase(string[i])?table[0][string[i]]:table[0][string[i].toLowerCase()].toUpperCase();
        } else if(table[1][string[i]] && document.getElementById('syms').checked) {
            res += table[1][string[i]];
        } else {
            res += string[i];
        };
    };
    return res;
};
function from(string) {
    let res = '';
    for(let i = 0; i < string.length; ++i) {
        if(revtable[string.slice(i,i+2).toLowerCase()]) {
            res += isLowerCase(string.slice(i,i+2))?revtable[string.slice(i,i+2)]:revtable[string.slice(i,i+2).toLowerCase()].toUpperCase();
            ++i
        } else {
            res += string[i];
        };
    };
    return res;
};